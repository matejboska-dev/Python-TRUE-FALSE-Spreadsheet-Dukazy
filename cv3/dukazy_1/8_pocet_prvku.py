##8. Jakým způsobem zjistím počet prvků v kolekci?

# Tuple
tuple_example = (1, 2, 3)
print(len(tuple_example))  

# List
list_example = [1, 2, 3]
print(len(list_example))  

# Set
set_example = {1, 2, 3}
print(len(set_example))  

# Dictionary
dict_example = {'a': 1, 'b': 2}
print(len(dict_example))  

# String
string_example = "abc"
print(len(string_example))  
